#!/usr/bin/python3

import numpy as np

arr = np.arange(0,11)
print(arr)
print(arr+arr)
print(arr-arr)
print(arr*arr)

## scaler operations
print(arr+100)
print(arr-100)

print(arr ** 2)

## universal array function
# square root of array
print(np.sqrt(arr))
print(np.exp(arr))

## trignometery 
# sin
print(np.sin(arr))
print(np.log(arr))

